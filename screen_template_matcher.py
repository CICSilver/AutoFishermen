def resolve_replay_region(args: argparse.Namespace, frame_shape) -> dict:
    frame_height, frame_width = frame_shape[:2]
    monitor = {"left": 0, "top": 0, "width": frame_width, "height": frame_height}
    if args.region_width is None or args.region_height is None:
        return {"left": 0, "top": 0, "width": frame_width, "height": frame_height}
    region = resolve_region(args, monitor)
    validate_region(region, monitor)
    return region


def list_replay_frames(replay_dir: Path) -> list[Path]:
    frames = [path for path in sorted(replay_dir.iterdir()) if path.is_file() and path.suffix.lower() in SUPPORTED_IMAGE_SUFFIXES]
    if not frames:
        raise ValueError(f"no supported replay frames found in: {replay_dir}")
    return frames


