def preprocess_gray(image):
    gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
    gray = cv2.GaussianBlur(gray, (3, 3), 0)
    return cv2.equalizeHist(gray)


def preprocess_edges(image):
    gray = preprocess_gray(image)
    return cv2.Canny(gray, 50, 150)


def preprocess_texture(image):
    gray = preprocess_gray(image)
    blur = cv2.GaussianBlur(gray, (0, 0), 3.0)
    high_pass = cv2.addWeighted(gray, 1.8, blur, -0.8, 0)
    grad_x = cv2.Sobel(high_pass, cv2.CV_32F, 1, 0, ksize=3)
    grad_y = cv2.Sobel(high_pass, cv2.CV_32F, 0, 1, ksize=3)
    magnitude = cv2.magnitude(grad_x, grad_y)
    magnitude = cv2.normalize(magnitude, None, 0, 255, cv2.NORM_MINMAX)
    magnitude = magnitude.astype(np.uint8)
    _, binary = cv2.threshold(magnitude, 0, 255, cv2.THRESH_BINARY + cv2.THRESH_OTSU)
    kernel = np.ones((3, 3), np.uint8)
    opened = cv2.morphologyEx(binary, cv2.MORPH_OPEN, kernel, iterations=1)
    return cv2.dilate(opened, kernel, iterations=1)


def preprocess_halo(image):
    hsv = cv2.cvtColor(image, cv2.COLOR_BGR2HSV)
    lower_yellow = np.array([18, 50, 130], dtype=np.uint8)
    upper_yellow = np.array([45, 255, 255], dtype=np.uint8)
    mask = cv2.inRange(hsv, lower_yellow, upper_yellow)

    lab = cv2.cvtColor(image, cv2.COLOR_BGR2LAB)
    l_channel, _a_channel, b_channel = cv2.split(lab)
    bright_mask = cv2.inRange(l_channel, 140, 255)
    warm_mask = cv2.inRange(b_channel, 140, 255)
    mask = cv2.bitwise_or(mask, cv2.bitwise_and(bright_mask, warm_mask))

    kernel_small = np.ones((3, 3), np.uint8)
    kernel_large = np.ones((5, 5), np.uint8)
    mask = cv2.morphologyEx(mask, cv2.MORPH_OPEN, kernel_small, iterations=1)
    mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel_large, iterations=2)
    return cv2.dilate(mask, kernel_small, iterations=1)


def preprocess_float_mask(image):
    hsv = cv2.cvtColor(image, cv2.COLOR_BGR2HSV)
    lab = cv2.cvtColor(image, cv2.COLOR_BGR2LAB)

    h_channel, s_channel, v_channel = cv2.split(hsv)
    l_channel, _a_channel, b_channel = cv2.split(lab)

    not_water = cv2.inRange(h_channel, 0, 179)
    blue_water = cv2.inRange(h_channel, 85, 135)
    not_water = cv2.bitwise_and(not_water, cv2.bitwise_not(blue_water))

    saturated = cv2.inRange(s_channel, 35, 255)
    bright = cv2.inRange(v_channel, 95, 255)
    warm_or_neutral = cv2.inRange(b_channel, 118, 255)
    light_outline = cv2.inRange(l_channel, 140, 255)

    edge = preprocess_edges(image)
    texture = preprocess_texture(image)

    color_candidate = cv2.bitwise_and(not_water, saturated)
    color_candidate = cv2.bitwise_and(color_candidate, bright)
    outline_candidate = cv2.bitwise_and(light_outline, cv2.bitwise_or(edge, texture))
    warm_candidate = cv2.bitwise_and(warm_or_neutral, cv2.bitwise_or(edge, texture))

    mask = cv2.bitwise_or(color_candidate, outline_candidate)
    mask = cv2.bitwise_or(mask, warm_candidate)

    kernel_small = np.ones((3, 3), np.uint8)
    kernel_large = np.ones((5, 5), np.uint8)
    mask = cv2.morphologyEx(mask, cv2.MORPH_OPEN, kernel_small, iterations=1)
    mask = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, kernel_large, iterations=2)
    return cv2.dilate(mask, kernel_small, iterations=1)


def prepare_variant(image, mode: str):
    if mode == "color":
        return image
    if mode == "gray":
        return preprocess_gray(image)
    if mode == "edge":
        return preprocess_edges(image)
    if mode == "combined":
        gray = preprocess_gray(image)
        edge = preprocess_edges(image)
        return cv2.addWeighted(gray, 0.7, edge, 0.3, 0)
    if mode == "texture":
        return preprocess_texture(image)
    if mode == "halo":
        return preprocess_halo(image)
    if mode == "float":
        return preprocess_float_mask(image)
    raise ValueError(f"unsupported variant mode: {mode}")

def load_config(config_path: Path) -> dict:
    if not config_path.exists():
        raise FileNotFoundError(f"config file not found: {config_path}")
    try:
        return json.loads(config_path.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        raise ValueError(f"failed to parse config JSON: {config_path}") from exc


def load_image_file(image_path: Path):
    if not image_path.exists():
        raise FileNotFoundError(f"image not found: {image_path}")
    if not image_path.is_file():
        raise ValueError(f"image path is not a file: {image_path}")

    image = cv2.imread(str(image_path), cv2.IMREAD_COLOR)
    if image is None:
        raise ValueError(f"failed to load image: {image_path}")
    return image


def normalize_manifest_entries(payload) -> list[dict]:
    if isinstance(payload, list):
        return payload
    if isinstance(payload, dict) and isinstance(payload.get("templates"), list):
        return payload["templates"]
    raise ValueError("template manifest must be a list or contain a 'templates' list")


def discover_template_files(template_dir: Path) -> list[Path]:
    return sorted(
        path for path in template_dir.iterdir() if path.is_file() and path.suffix.lower() in SUPPORTED_IMAGE_SUFFIXES
    )


def load_templates(args: argparse.Namespace) -> tuple[list[dict], str]:
    template_entries = []
    source_note = ""

    if args.template_dir:
        template_dir = Path(args.template_dir).expanduser().resolve()
        if not template_dir.exists() or not template_dir.is_dir():
            raise ValueError(f"template directory not found: {template_dir}")

        if args.template:
            print("Template directory provided; ignoring --template and using directory templates.", flush=True)

        manifest_path = None
        if args.manifest:
            manifest_path = Path(args.manifest).expanduser().resolve()
        else:
            candidate_manifest = template_dir / "manifest.json"
            if candidate_manifest.exists():
                manifest_path = candidate_manifest

        manifest_entries: list[dict] = []
        if manifest_path is not None:
            payload = load_config(manifest_path)
            manifest_entries = normalize_manifest_entries(payload)

        if manifest_entries:
            for index, entry in enumerate(manifest_entries):
                if not isinstance(entry, dict):
                    raise ValueError("every template manifest entry must be an object")
                raw_path = entry.get("path")
                if not raw_path:
                    raise ValueError(f"template manifest entry #{index + 1} is missing 'path'")
                template_path = (manifest_path.parent / raw_path).resolve()
                template_id = entry.get("id") or template_path.stem
                category = entry.get("category", "float")
                match_mode = entry.get("match_mode", args.match_mode)
                candidate_threshold = float(entry.get("candidate_threshold", args.candidate_threshold))
                tracking_threshold = float(entry.get("tracking_threshold", args.tracking_threshold))
                image = load_image_file(template_path)
                template_entries.append(
                    {
                        "id": str(template_id),
                        "path": template_path,
                        "category": str(category),
                        "match_mode": str(match_mode),
                        "candidate_threshold": candidate_threshold,
                        "tracking_threshold": tracking_threshold,
                        "image": image,
                        "width": int(image.shape[1]),
                        "height": int(image.shape[0]),
                    }
                )
            source_note = f"manifest={manifest_path}"
        else:
            for template_path in discover_template_files(template_dir):
                image = load_image_file(template_path)
                template_entries.append(
                    {
                        "id": template_path.stem,
                        "path": template_path,
                        "category": "float",
                        "match_mode": args.match_mode,
                        "candidate_threshold": args.candidate_threshold,
                        "tracking_threshold": args.tracking_threshold,
                        "image": image,
                        "width": int(image.shape[1]),
                        "height": int(image.shape[0]),
                    }
                )
            source_note = f"directory={template_dir}"
    else:
        template_path = Path(args.template).expanduser().resolve()
        image = load_image_file(template_path)
        template_entries.append(
            {
                "id": template_path.stem,
                "path": template_path,
                "category": "float",
                "match_mode": args.match_mode,
                "candidate_threshold": args.candidate_threshold,
                "tracking_threshold": args.tracking_threshold,
                "image": image,
                "width": int(image.shape[1]),
                "height": int(image.shape[0]),
            }
        )
        source_note = f"template={template_path}"

    if not template_entries:
        raise ValueError("no templates were loaded")

    for entry in template_entries:
        if entry["match_mode"] not in ("color", "gray", "edge", "texture", "halo", "float", "combined"):
            raise ValueError(f"unsupported template match_mode for {entry['id']}: {entry['match_mode']}")
        if not 0.0 <= entry["candidate_threshold"] <= 1.0:
            raise ValueError(f"candidate_threshold for {entry['id']} must be between 0.0 and 1.0")
        if not 0.0 <= entry["tracking_threshold"] <= 1.0:
            raise ValueError(f"tracking_threshold for {entry['id']} must be between 0.0 and 1.0")

    return template_entries, source_note


def describe_template_entries(template_entries: list[dict]) -> str:
    parts = []
    for entry in template_entries:
        parts.append(
            f"{entry['id']}[{entry['match_mode']} c={entry['candidate_threshold']:.2f} t={entry['tracking_threshold']:.2f}]"
        )
    return ", ".join(parts)


def get_primary_monitor(sct: mss.mss) -> dict:
    if len(sct.monitors) < 2:
        raise RuntimeError("no primary monitor information available")
    return sct.monitors[1]


def resolve_region(args: argparse.Namespace, monitor: dict) -> dict:
    if args.region_width is None or args.region_height is None:
        raise ValueError("region width and height must be provided directly or via --config")

    if args.left is not None or args.top is not None:
        if args.left is None or args.top is None:
            raise ValueError("left and top must be provided together")
        return {
            "left": args.left,
            "top": args.top,
            "width": args.region_width,
            "height": args.region_height,
        }

    monitor_left = monitor["left"]
    monitor_top = monitor["top"]
    monitor_width = monitor["width"]
    monitor_height = monitor["height"]
    if args.region_width > monitor_width or args.region_height > monitor_height:
        raise ValueError("capture region cannot be larger than the primary monitor")

    left = monitor_left + (monitor_width - args.region_width) // 2
    top = monitor_top + (monitor_height - args.region_height) // 2
    return {"left": left, "top": top, "width": args.region_width, "height": args.region_height}


def apply_config_defaults(args: argparse.Namespace) -> argparse.Namespace:
    if not args.config:
        return args

    config_path = Path(args.config).expanduser().resolve()
    config = load_config(config_path)
    capture_region = config.get("capture_region", {})
    matcher_defaults = config.get("matcher_defaults", {})

    if args.left is None:
        args.left = capture_region.get("left")
    if args.top is None:
        args.top = capture_region.get("top")
    if args.region_width is None:
        args.region_width = capture_region.get("width")
    if args.region_height is None:
        args.region_height = capture_region.get("height")
    if args.fps == 5.0 and "fps" in matcher_defaults:
        args.fps = matcher_defaults["fps"]
    if args.threshold == 0.8 and "threshold" in matcher_defaults:
        args.threshold = matcher_defaults["threshold"]
    if args.max_candidates == 8 and "max_candidates" in matcher_defaults:
        args.max_candidates = matcher_defaults["max_candidates"]
    if args.sound_threshold == 80.0 and "sound_threshold" in matcher_defaults:
        args.sound_threshold = matcher_defaults["sound_threshold"]
    if args.sound_notification_duration == 2.0 and "sound_notification_duration" in matcher_defaults:
        args.sound_notification_duration = matcher_defaults["sound_notification_duration"]
    if args.dataset_dir == "data" and "dataset_dir" in matcher_defaults:
        args.dataset_dir = matcher_defaults["dataset_dir"]
    if not args.save_candidate_dataset and matcher_defaults.get("save_candidate_dataset") is True:
        args.save_candidate_dataset = True
    if args.enable_sound_monitor and matcher_defaults.get("enable_sound_monitor") is False:
        args.enable_sound_monitor = False
    if args.show_preview and matcher_defaults.get("show_preview") is False:
        args.show_preview = False

    return args


def validate_region(region: dict, monitor: dict) -> None:
    if region["width"] <= 0 or region["height"] <= 0:
        raise ValueError("capture region dimensions must be positive")
    if region["left"] < monitor["left"] or region["top"] < monitor["top"]:
        raise ValueError("capture region cannot start outside the primary monitor")
    if region["left"] + region["width"] > monitor["left"] + monitor["width"]:
        raise ValueError("capture region exceeds the primary monitor width")
    if region["top"] + region["height"] > monitor["top"] + monitor["height"]:
        raise ValueError("capture region exceeds the primary monitor height")


def ensure_templates_fit(region: dict, template_entries: list[dict]) -> None:
    for entry in template_entries:
        if entry["width"] > region["width"] or entry["height"] > region["height"]:
            raise ValueError(
                f"template image must not be larger than the capture region: {entry['path']}"
            )


def capture_region(sct: mss.mss, region: dict):
    raw = np.array(sct.grab(region))
    return cv2.cvtColor(raw, cv2.COLOR_BGRA2BGR)


def run_single_match(frame_variant, template_variant) -> tuple[float, tuple[int, int], np.ndarray]:
    result = cv2.matchTemplate(frame_variant, template_variant, MATCH_METHOD)
    _, max_val, _, max_loc = cv2.minMaxLoc(result)
    return float(max_val), max_loc, result


def match_template(frame, template, match_mode: str) -> dict:
    if match_mode == "combined":
        gray_score, gray_top_left, gray_result = run_single_match(
            prepare_variant(frame, "gray"),
            prepare_variant(template, "gray"),
        )
        edge_score, edge_top_left, _edge_result = run_single_match(
            prepare_variant(frame, "edge"),
            prepare_variant(template, "edge"),
        )
        fused_score = gray_score * 0.7 + edge_score * 0.3
        fused_top_left = gray_top_left if gray_score >= edge_score else edge_top_left
        return {
            "best_mode": "combined",
            "score": fused_score,
            "top_left": fused_top_left,
            "result_map": gray_result,
            "scores": {
                "gray": gray_score,
                "edge": edge_score,
                "combined": fused_score,
            },
        }

    matches = {}
    for mode in ("color", "gray", "edge", "texture", "halo", "float"):
        if match_mode == mode:
            score, top_left, result = run_single_match(prepare_variant(frame, mode), prepare_variant(template, mode))
            matches[mode] = {"score": score, "top_left": top_left, "result_map": result}

    if not matches:
        raise ValueError(f"unsupported match mode: {match_mode}")

    best_mode = max(matches.items(), key=lambda item: item[1]["score"])[0]
    best = matches[best_mode]
    return {
        "best_mode": best_mode,
        "score": best["score"],
        "top_left": best["top_left"],
        "result_map": best["result_map"],
        "scores": {name: details["score"] for name, details in matches.items()},
    }

def clip_search_region(region: dict, frame_shape, template_shape, padding: int) -> dict:
    frame_height, frame_width = frame_shape[:2]
    template_height, template_width = template_shape[:2]
    left = max(0, region["left"] - padding)
    top = max(0, region["top"] - padding)
    right = min(frame_width, region["left"] + region["width"] + padding)
    bottom = min(frame_height, region["top"] + region["height"] + padding)

    if right - left < template_width:
        right = min(frame_width, left + template_width)
        left = max(0, right - template_width)
    if bottom - top < template_height:
        bottom = min(frame_height, top + template_height)
        top = max(0, bottom - template_height)

    return {"left": left, "top": top, "width": right - left, "height": bottom - top}


def crop_frame(frame, search_region: dict):
    left = search_region["left"]
    top = search_region["top"]
    right = left + search_region["width"]
    bottom = top + search_region["height"]
    return frame[top:bottom, left:right]


def absolute_rect(top_left: tuple[int, int], template_shape, offset: tuple[int, int]) -> dict:
    template_height, template_width = template_shape[:2]
    left = top_left[0] + offset[0]
    top = top_left[1] + offset[1]
    return {
        "left": left,
        "top": top,
        "width": template_width,
        "height": template_height,
        "right": left + template_width,
        "bottom": top + template_height,
        "center_x": left + template_width / 2.0,
        "center_y": top + template_height / 2.0,
    }


def build_candidate_mask(float_mask, halo_mask, mode: str):
    if mode == "float":
        return float_mask
    if mode == "halo":
        return halo_mask
    if mode == "intersection":
        return cv2.bitwise_and(float_mask, halo_mask)
    raise ValueError(f"unsupported candidate-mask-mode: {mode}")


def get_template_pool(
    template_entries: list[dict],
    active_template_id: str | None,
    allow_template_switch: bool,
) -> tuple[list[dict], dict | None]:
    active_entry = next((entry for entry in template_entries if entry["id"] == active_template_id), None)
    if active_entry is not None and not allow_template_switch:
        return [active_entry], active_entry
    return template_entries, active_entry


def build_candidate_regions(
    mask,
    frame,
    halo_mask,
    template_entries: list[dict],
    tracked_rect: dict | None,
    search_padding: int,
    candidate_min_area: int,
    max_candidates: int,
    use_halo_gate: bool,
) -> tuple[list[dict], dict]:
    frame_height, frame_width = frame.shape[:2]
    contours, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
    gray_frame = preprocess_gray(frame)

    widths = [entry["width"] for entry in template_entries]
    heights = [entry["height"] for entry in template_entries]
    target_width = float(sum(widths) / max(1, len(widths)))
    target_height = float(sum(heights) / max(1, len(heights)))

    kept = []
    debug = {
        "raw_candidates": 0,
        "post_size_candidates": 0,
        "gated_candidates": 0,
        "rejected_by_aspect": 0,
        "rejected_by_halo": 0,
    }

    for contour in contours:
        x, y, w, h = cv2.boundingRect(contour)
        debug["raw_candidates"] += 1
        area = w * h
        if area < candidate_min_area:
            continue
        if w > frame_width * 0.4 or h > frame_height * 0.4:
            continue

        aspect_ratio = w / max(h, 1)
        if aspect_ratio < 0.2 or aspect_ratio > 4.5:
            debug["rejected_by_aspect"] += 1
            continue

        expanded_left = max(0, x - max(search_padding // 2, int(target_width // 3)))
        expanded_top = max(0, y - max(search_padding // 2, int(target_height // 3)))
        expanded_right = min(frame_width, x + w + max(search_padding // 2, int(target_width // 3)))
        expanded_bottom = min(frame_height, y + h + max(search_padding // 2, int(target_height // 3)))

        crop_width = expanded_right - expanded_left
        crop_height = expanded_bottom - expanded_top
        if crop_width <= 0 or crop_height <= 0:
            continue

        candidate_gray = gray_frame[expanded_top:expanded_bottom, expanded_left:expanded_right]
        candidate_halo = halo_mask[expanded_top:expanded_bottom, expanded_left:expanded_right]
        brightness = float(candidate_gray.mean()) if candidate_gray.size else 0.0
        halo_overlap = float((candidate_halo > 0).sum()) / float(candidate_halo.size) if candidate_halo.size else 0.0
        size_delta = abs(crop_width - target_width) + abs(crop_height - target_height)
        brightness_penalty = abs(brightness - 160.0)

        candidate = {
            "left": expanded_left,
            "top": expanded_top,
            "width": crop_width,
            "height": crop_height,
            "area": area,
            "aspect_ratio": aspect_ratio,
            "brightness": brightness,
            "halo_overlap": halo_overlap,
            "size_delta": size_delta,
        }

        if tracked_rect is not None:
            center_x = candidate["left"] + candidate["width"] / 2.0
            center_y = candidate["top"] + candidate["height"] / 2.0
            candidate["priority"] = abs(center_x - tracked_rect["center_x"]) + abs(center_y - tracked_rect["center_y"])
        else:
            candidate["priority"] = 0.0

        candidate["brightness_penalty"] = brightness_penalty
        debug["post_size_candidates"] += 1

        if use_halo_gate and halo_overlap <= 0.0:
            debug["rejected_by_halo"] += 1
            continue

        kept.append(candidate)

    if tracked_rect is not None:
        kept.sort(key=lambda item: (item["priority"], item["size_delta"], -item["halo_overlap"], item["brightness_penalty"], -item["area"]))
    else:
        kept.sort(key=lambda item: (item["size_delta"], -item["halo_overlap"], item["brightness_penalty"], -item["area"]))

    kept = kept[:max_candidates]
    debug["gated_candidates"] = len(kept)
    return kept, debug


def enrich_match_result(match_info: dict, template_entry: dict, search_region: dict | None, source: str) -> dict:
    match_info["template_id"] = template_entry["id"]
    match_info["template_path"] = str(template_entry["path"])
    match_info["template_category"] = template_entry["category"]
    match_info["template_image"] = template_entry["image"]
    match_info["candidate_threshold"] = template_entry["candidate_threshold"]
    match_info["tracking_threshold"] = template_entry["tracking_threshold"]
    match_info["search_source"] = source
    match_info["search_region"] = search_region
    return match_info


def match_templates_in_region(
    frame,
    template_entries: list[dict],
    fallback_match_mode: str,
    search_region: dict | None,
    source: str,
) -> dict | None:
    best_match = None
    template_scores = []
    for template_entry in template_entries:
        template = template_entry["image"]
        if frame.shape[1] < template.shape[1] or frame.shape[0] < template.shape[0]:
            continue
        match_info = match_template(frame, template, template_entry.get("match_mode", fallback_match_mode))
        template_scores.append(
            {
                "template_id": template_entry["id"],
                "score": float(match_info["score"]),
                "best_mode": match_info["best_mode"],
            }
        )
        offset = (search_region["left"], search_region["top"]) if search_region is not None else (0, 0)
        match_info["target_rect"] = absolute_rect(match_info["top_left"], template.shape, offset)
        enrich_match_result(match_info, template_entry, search_region, source)
        if best_match is None or match_info["score"] > best_match["score"]:
            best_match = match_info
    if best_match is not None:
        best_match["template_scores"] = sorted(template_scores, key=lambda item: item["score"], reverse=True)
    return best_match


def match_in_candidates(
    frame,
    template_entries: list[dict],
    fallback_match_mode: str,
    candidates: list[dict],
) -> tuple[dict | None, list[dict]]:
    best_match = None
    candidate_evaluations = []
    for index, candidate in enumerate(candidates):
        candidate_frame = crop_frame(frame, candidate)
        match_info = match_templates_in_region(
            frame=candidate_frame,
            template_entries=template_entries,
            fallback_match_mode=fallback_match_mode,
            search_region=candidate,
            source="candidate",
        )
        evaluation = {
            "candidate_index": index,
            "candidate_box": dict(candidate),
            "score": 0.0,
            "threshold": 0.0,
            "accepted": False,
            "template_id": None,
            "best_mode": None,
        }
        if match_info is None:
            candidate_evaluations.append(evaluation)
            continue
        match_info["candidate_box"] = candidate
        evaluation.update(
            {
                "score": float(match_info["score"]),
                "threshold": float(match_info["candidate_threshold"]),
                "accepted": bool(match_info["score"] >= match_info["candidate_threshold"]),
                "template_id": match_info["template_id"],
                "best_mode": match_info["best_mode"],
            }
        )
        candidate_evaluations.append(evaluation)
        if best_match is None or match_info["score"] > best_match["score"]:
            best_match = match_info
    if best_match is not None:
        best_match["candidate_evaluations"] = candidate_evaluations
    return best_match, candidate_evaluations


def build_step_result(
    match_info: dict,
    float_mask,
    halo_mask,
    candidate_boxes: list[dict],
    candidate_evaluations: list[dict],
    candidate_debug: dict,
    failure_reason: str | None,
    candidate_mask_mode: str,
    use_halo_gate: bool,
) -> dict:
    match_info["float_mask"] = float_mask
    match_info["halo_mask"] = halo_mask
    match_info["candidate_boxes"] = candidate_boxes
    match_info["candidate_evaluations"] = candidate_evaluations
    match_info["candidate_debug"] = candidate_debug
    match_info["failure_reason"] = failure_reason
    match_info["candidate_mask_mode"] = candidate_mask_mode
    match_info["halo_gate_enabled"] = use_halo_gate
    return match_info


def detect_target(
    frame,
    template_entries: list[dict],
    match_mode: str,
    tracked_rect: dict | None,
    search_padding: int,
    use_halo_gate: bool,
    candidate_mask_mode: str,
    candidate_min_area: int,
    max_candidates: int,
    active_template_id: str | None,
    allow_template_switch: bool,
) -> dict:
    float_mask = preprocess_float_mask(frame)
    halo_mask = preprocess_halo(frame)
    base_mask = build_candidate_mask(float_mask, halo_mask, candidate_mask_mode)
    template_pool, active_entry = get_template_pool(template_entries, active_template_id, allow_template_switch)
    candidate_boxes, candidate_debug = build_candidate_regions(
        mask=base_mask,
        frame=frame,
        halo_mask=halo_mask,
        template_entries=template_pool,
        tracked_rect=tracked_rect,
        search_padding=search_padding,
        candidate_min_area=candidate_min_area,
        max_candidates=max_candidates,
        use_halo_gate=use_halo_gate,
    )
    candidate_debug["mask_mode"] = candidate_mask_mode
    candidate_debug["halo_gate_enabled"] = use_halo_gate
    candidate_debug["active_template_locked"] = active_entry["id"] if active_entry is not None else None
    candidate_evaluations = []

    if candidate_boxes:
        candidate_match, candidate_evaluations = match_in_candidates(
            frame=frame,
            template_entries=template_pool,
            fallback_match_mode=match_mode,
            candidates=candidate_boxes,
        )
        candidate_debug["candidate_exportable"] = len(candidate_evaluations)
        candidate_debug["candidate_float_labels"] = sum(1 for item in candidate_evaluations if item["accepted"])
        if candidate_match is not None:
            candidate_debug["candidate_score"] = candidate_match["score"]
            candidate_debug["candidate_template_id"] = candidate_match["template_id"]
            if candidate_match["score"] >= candidate_match["candidate_threshold"]:
                candidate_debug["candidate_accepted"] = True
                return build_step_result(
                    candidate_match,
                    float_mask,
                    halo_mask,
                    candidate_boxes,
                    candidate_evaluations,
                    candidate_debug,
                    None,
                    candidate_mask_mode,
                    use_halo_gate,
                )
            candidate_failure = "candidate_score_too_low"
        else:
            candidate_failure = "candidate_match_failed"
    else:
        candidate_failure = "no_candidates"

    if tracked_rect is not None and template_pool:
        max_template_height = max(entry["height"] for entry in template_pool)
        max_template_width = max(entry["width"] for entry in template_pool)
        template_shape = (max_template_height, max_template_width, 3)
        local_region = clip_search_region(tracked_rect, frame.shape, template_shape, search_padding)
        local_frame = crop_frame(frame, local_region)
        local_match = match_templates_in_region(
            frame=local_frame,
            template_entries=template_pool,
            fallback_match_mode=match_mode,
            search_region=local_region,
            source="local",
        )
        if local_match is not None and local_match["score"] >= local_match["candidate_threshold"]:
            return build_step_result(
                local_match,
                float_mask,
                halo_mask,
                candidate_boxes,
                candidate_evaluations,
                candidate_debug,
                candidate_failure,
                candidate_mask_mode,
                use_halo_gate,
            )

    global_match = match_templates_in_region(
        frame=frame,
        template_entries=template_pool,
        fallback_match_mode=match_mode,
        search_region=None,
        source="global",
    )
    if global_match is None:
        raise RuntimeError("no template could be matched in the current frame")
    return build_step_result(
        global_match,
        float_mask,
        halo_mask,
        candidate_boxes,
        candidate_evaluations,
        candidate_debug,
        candidate_failure,
        candidate_mask_mode,
        use_halo_gate,
    )


