def write_replay_output(output_path: Path, rows: list[dict]) -> None:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    if output_path.suffix.lower() == ".json":
        output_path.write_text(json.dumps(rows, ensure_ascii=False, indent=2), encoding="utf-8")
        return

    fieldnames = list(rows[0].keys()) if rows else []
    with output_path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def build_sound_monitor_state(args: argparse.Namespace) -> dict:
    return {
        "enabled": bool(args.enable_sound_monitor),
        "threshold": float(args.sound_threshold),
        "notification_duration": float(args.sound_notification_duration),
        "current_level": 0.0,
        "notification_text": None,
        "notification_until": 0.0,
        "warning_printed": False,
        "device_error": None,
        "lock": threading.Lock(),
        "stop_event": threading.Event(),
        "thread": None,
    }


def trigger_sound_notification(sound_monitor: dict, text: str) -> None:
    now = time.monotonic()
    with sound_monitor["lock"]:
        sound_monitor["notification_text"] = text
        sound_monitor["notification_until"] = now + sound_monitor["notification_duration"]


def sound_monitor_loop(sound_monitor: dict) -> None:
    if sd is None:
        return

    def audio_callback(indata, frames, time_info, status):
        volume_norm = float(np.linalg.norm(indata) * 10)
        with sound_monitor["lock"]:
            sound_monitor["current_level"] = volume_norm
        if volume_norm > sound_monitor["threshold"]:
            trigger_sound_notification(
                sound_monitor,
                f"Sound Trigger: {volume_norm:.1f} > {sound_monitor['threshold']:.1f}",
            )

    stop_event = sound_monitor["stop_event"]
    while not stop_event.is_set():
        try:
            with sd.InputStream(callback=audio_callback):
                while not stop_event.is_set():
                    sd.sleep(200)
        except Exception as exc:
            sound_monitor["device_error"] = str(exc)
            if not sound_monitor["warning_printed"]:
                print(f"Sound monitor disabled: {exc}", flush=True)
                sound_monitor["warning_printed"] = True
            trigger_sound_notification(sound_monitor, "Sound Monitor Error")
            return


def start_sound_monitor(sound_monitor: dict) -> None:
    if not sound_monitor["enabled"]:
        return
    if sd is None:
        if not sound_monitor["warning_printed"]:
            print("Sound monitor unavailable: sounddevice is not installed.", flush=True)
            sound_monitor["warning_printed"] = True
        sound_monitor["enabled"] = False
        return
    thread = threading.Thread(target=sound_monitor_loop, args=(sound_monitor,), daemon=True)
    sound_monitor["thread"] = thread
    thread.start()


def stop_sound_monitor(sound_monitor: dict) -> None:
    sound_monitor["stop_event"].set()
    thread = sound_monitor.get("thread")
    if thread is not None and thread.is_alive():
        thread.join(timeout=1.0)


def refresh_sound_notification(tracking_state: dict, sound_monitor: dict) -> None:
    now = time.monotonic()
    with sound_monitor["lock"]:
        level = float(sound_monitor.get("current_level", 0.0))
        text = sound_monitor["notification_text"]
        until = sound_monitor["notification_until"]
        device_error = sound_monitor.get("device_error")
        enabled = bool(sound_monitor.get("enabled"))
        threshold = float(sound_monitor.get("threshold", tracking_state["sound_threshold"]))

    tracking_state["sound_level"] = level
    tracking_state["sound_threshold"] = threshold
    tracking_state["sound_monitor_enabled"] = enabled
    tracking_state["sound_device_error"] = device_error

    if text and until > now:
        tracking_state["sound_notification_text"] = text
        tracking_state["sound_notification_until"] = until
    elif tracking_state.get("sound_notification_until", 0.0) <= now:
        tracking_state["sound_notification_text"] = None
        tracking_state["sound_notification_until"] = 0.0


def run_live_capture(args: argparse.Namespace, template_entries: list[dict], source_note: str) -> int:
    with mss.mss() as sct:
        monitor = get_primary_monitor(sct)
        region = resolve_region(args, monitor)
        validate_region(region, monitor)
        ensure_templates_fit(region, template_entries)

        interval = 1.0 / args.fps
        next_capture_at = time.perf_counter()
        last_loop_at = None
        tracking_state = initialize_tracking_state(args)
        dataset_exporter = build_candidate_dataset_exporter(args)
        sound_monitor = build_sound_monitor_state(args)
        start_sound_monitor(sound_monitor)

        print(
            "Starting capture with "
            f"{source_note}, templates={len(template_entries)}, region={region['width']}x{region['height']} "
            f"at ({region['left']}, {region['top']}), fps={args.fps:.2f}, "
            f"threshold={args.threshold:.2f}, default-tracking-threshold={args.tracking_threshold:.2f}, "
            f"candidate-mask={args.candidate_mask_mode}, halo-gate={'on' if args.use_halo_gate else 'off'}, "
            f"max-candidates={args.max_candidates}, "
            f"dataset-export={'on' if dataset_exporter['enabled'] else 'off'}, "
            f"sound-monitor={'on' if sound_monitor['enabled'] else 'off'}, "
            f"preview={'on' if args.show_preview else 'off'}"
        )
        print(f"Loaded templates: {describe_template_entries(template_entries)}", flush=True)
        if dataset_exporter["enabled"]:
            print(
                f"Candidate dataset export enabled: float={dataset_exporter['float_dir']} "
                f"non_float={dataset_exporter['non_float_dir']}",
                flush=True,
            )

        frame_index = 0
        try:
            while True:
                refresh_sound_notification(tracking_state, sound_monitor)
                now = time.perf_counter()
                remaining = next_capture_at - now
                if remaining > 0:
                    time.sleep(min(remaining, 0.01))
                    continue

                frame = capture_region(sct, region)
                match_info = detect_target(
                    frame=frame,
                    template_entries=template_entries,
                    match_mode=args.match_mode,
                    tracked_rect=tracking_state["tracked_rect"] if tracking_state["locked"] else None,
                    search_padding=args.search_padding,
                    use_halo_gate=args.use_halo_gate,
                    candidate_mask_mode=args.candidate_mask_mode,
                    candidate_min_area=args.candidate_min_area,
                    max_candidates=args.max_candidates,
                    active_template_id=tracking_state["active_template_id"],
                    allow_template_switch=allow_template_switch(tracking_state),
                )
                save_candidate_dataset_samples(frame, match_info, dataset_exporter, frame_index, "live")
                record_match_state(tracking_state, match_info)
                update_tracking_state(tracking_state, match_info, args, frame_index)
                announce_bite_event(match_info, tracking_state, frame_index, "live")

                loop_now = time.perf_counter()
                current_fps = 0.0 if last_loop_at is None else 1.0 / max(loop_now - last_loop_at, 1e-6)
                last_loop_at = loop_now

                print(format_status_line(match_info, tracking_state), flush=True)

                if args.show_preview:
                    capture_preview = draw_overlay(
                        frame=frame,
                        score=match_info["score"],
                        target_rect=match_info["target_rect"],
                        threshold=args.threshold,
                        current_fps=current_fps,
                        best_mode=match_info["best_mode"],
                        tracking_state=tracking_state,
                    )
                    dashboard = build_match_dashboard(
                        frame=frame,
                        matched_overlay=capture_preview,
                        result_map=match_info["result_map"],
                        match_info=match_info,
                        tracking_state=tracking_state,
                    )
                    cv2.imshow(WINDOW_NAME_CAPTURE, capture_preview)
                    cv2.imshow(WINDOW_NAME_DASHBOARD, dashboard)
                    if should_exit_from_preview():
                        break
                    if cv2.getWindowProperty(WINDOW_NAME_CAPTURE, cv2.WND_PROP_VISIBLE) < 1:
                        break
                    if cv2.getWindowProperty(WINDOW_NAME_DASHBOARD, cv2.WND_PROP_VISIBLE) < 1:
                        break

                next_capture_at = max(next_capture_at + interval, time.perf_counter())
                frame_index += 1
        finally:
            stop_sound_monitor(sound_monitor)
            if args.show_preview:
                cv2.destroyAllWindows()

    return 0


def run_replay(args: argparse.Namespace, template_entries: list[dict], source_note: str) -> int:
    replay_dir = Path(args.replay_dir).expanduser().resolve()
    frame_paths = list_replay_frames(replay_dir)
    tracking_state = initialize_tracking_state(args)
    dataset_exporter = build_candidate_dataset_exporter(args)
    rows = []

    first_frame = load_image_file(frame_paths[0])
    region = resolve_replay_region(args, first_frame.shape)
    ensure_templates_fit(region, template_entries)

    print(
        "Starting replay with "
        f"{source_note}, templates={len(template_entries)}, frames={len(frame_paths)}, "
        f"region={region['width']}x{region['height']} at ({region['left']}, {region['top']}), "
        f"dataset-export={'on' if dataset_exporter['enabled'] else 'off'}"
    )
    print(f"Loaded templates: {describe_template_entries(template_entries)}", flush=True)
    if dataset_exporter["enabled"]:
        print(
            f"Candidate dataset export enabled: float={dataset_exporter['float_dir']} "
            f"non_float={dataset_exporter['non_float_dir']}",
            flush=True,
        )

    for frame_index, frame_path in enumerate(frame_paths):
        full_frame = load_image_file(frame_path)
        cropped_frame = crop_frame(full_frame, region)
        match_info = detect_target(
            frame=cropped_frame,
            template_entries=template_entries,
            match_mode=args.match_mode,
            tracked_rect=tracking_state["tracked_rect"] if tracking_state["locked"] else None,
            search_padding=args.search_padding,
            use_halo_gate=args.use_halo_gate,
            candidate_mask_mode=args.candidate_mask_mode,
            candidate_min_area=args.candidate_min_area,
            max_candidates=args.max_candidates,
            active_template_id=tracking_state["active_template_id"],
            allow_template_switch=allow_template_switch(tracking_state),
        )
        save_candidate_dataset_samples(cropped_frame, match_info, dataset_exporter, frame_index, "replay")
        record_match_state(tracking_state, match_info)
        update_tracking_state(tracking_state, match_info, args, frame_index)
        announce_bite_event(match_info, tracking_state, frame_index, "replay")
        row = serialize_replay_row(frame_path, frame_index, match_info, tracking_state)
        rows.append(row)
        print(
            f"[{frame_index:04d}] frame={frame_path.name} score={match_info['score']:.4f} "
            f"template={match_info['template_id']} source={tracking_state['search_source']} "
            f"lock={tracking_state['locked']} bite={tracking_state['bite_event']}"
        )

    if args.replay_output:
        output_path = Path(args.replay_output).expanduser().resolve()
        write_replay_output(output_path, rows)
        print(f"Replay results written to: {output_path}")

    return 0


def run(args: argparse.Namespace) -> int:
    ensure_dependencies_loaded()
    args = apply_config_defaults(args)
    args = prepare_path_arguments(args)
    validate_args(args)
    template_entries, source_note = load_templates(args)

    if args.replay_dir:
        return run_replay(args, template_entries, source_note)
    return run_live_capture(args, template_entries, source_note)


def main() -> int:
    try:
        return run(parse_args())
    except KeyboardInterrupt:
        print("Interrupted by user.")
        return 130
    except Exception as exc:
        print(f"Error: {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
