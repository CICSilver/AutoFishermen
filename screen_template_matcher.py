def save_candidate_dataset_samples(frame, match_info: dict, dataset_exporter: dict, frame_index: int, source_label: str) -> None:
    if not dataset_exporter.get("enabled"):
        return

    evaluations = match_info.get("candidate_evaluations", [])
    for evaluation in evaluations:
        candidate_box = evaluation.get("candidate_box")
        if not candidate_box:
            continue
        crop = crop_frame(frame, candidate_box)
        if crop.size == 0:
            continue

        interpolation = cv2.INTER_AREA if crop.shape[0] > DATASET_CROP_SIZE or crop.shape[1] > DATASET_CROP_SIZE else cv2.INTER_LINEAR
        resized = cv2.resize(crop, (DATASET_CROP_SIZE, DATASET_CROP_SIZE), interpolation=interpolation)

        dataset_exporter["counter"] += 1
        label_dir = dataset_exporter["float_dir"] if evaluation["accepted"] else dataset_exporter["non_float_dir"]
        template_id = evaluation.get("template_id") or "none"
        filename = (
            f"{source_label}_f{frame_index:06d}_c{evaluation['candidate_index']:02d}_"
            f"n{dataset_exporter['counter']:07d}_{template_id}_"
            f"s{evaluation['score']:.4f}_t{evaluation['threshold']:.4f}.png"
        )
        cv2.imwrite(str(label_dir / filename), resized)

def update_tracking_state(state: dict, match_info: dict, args: argparse.Namespace, frame_index: int) -> dict:
    score = match_info["score"]
    target_rect = match_info["target_rect"]
    tracking_threshold = match_info["tracking_threshold"]
    locked_before = state["locked"]
    bite_before = state["bite_event"]
    valid_detection = score >= tracking_threshold and match_info["search_source"] in ("candidate", "local", "global")

    if score >= tracking_threshold:
        state["locked"] = True
        state["tracked_rect"] = target_rect
        state["lost_frames"] = 0
        if state["active_template_id"] != match_info["template_id"]:
            if state["active_template_id"] is not None:
                state["template_switches"] += 1
            state["active_template_id"] = match_info["template_id"]
        state["template_miss_frames"] = 0
    else:
        state["lost_frames"] += 1
        state["template_miss_frames"] += 1
        if state["lost_frames"] >= args.reacquire_frames:
            state["locked"] = False
            state["tracked_rect"] = None

    if valid_detection:
        point = {
            "frame_index": frame_index,
            "center_x": target_rect["center_x"],
            "center_y": target_rect["center_y"],
            "score": score,
            "best_mode": match_info["best_mode"],
            "search_source": match_info["search_source"],
            "locked": state["locked"],
            "template_id": match_info["template_id"],
        }
        state["history"].append(point)
        if len(state["history"]) > args.history_size:
            state["history"].popleft()

    bite = detect_bite_event(state["history"], match_info["tracking_threshold"], args, state["locked"])
    state["bite_event"] = bite["triggered"]
    state["bite_metrics"] = bite
    state["just_locked"] = state["locked"] and not locked_before
    state["just_bite_event"] = state["bite_event"] and not bite_before
    state["valid_detection"] = valid_detection
    return state


def detect_bite_event(history: deque, tracking_threshold: float, args: argparse.Namespace, is_locked: bool) -> dict:
    if not is_locked or len(history) < args.bite_window:
        return {"triggered": False, "drop_pixels": 0.0, "speed": 0.0}

    recent = list(history)[-args.bite_window :]
    if any(item["score"] < tracking_threshold for item in recent):
        return {"triggered": False, "drop_pixels": 0.0, "speed": 0.0}

    start = recent[0]
    end = recent[-1]
    drop_pixels = end["center_y"] - start["center_y"]
    frame_span = max(1, end["frame_index"] - start["frame_index"])
    speed = drop_pixels / frame_span

    stable_prefix = recent[:-2]
    if stable_prefix:
        ys = [item["center_y"] for item in stable_prefix]
        prefix_range = max(ys) - min(ys)
    else:
        prefix_range = 0.0

    triggered = drop_pixels >= args.bite_drop_pixels and prefix_range <= args.bite_drop_pixels * 0.6 and speed > 1.5
    return {"triggered": triggered, "drop_pixels": drop_pixels, "speed": speed}


def draw_tracking_path(frame, history: deque) -> None:
    points = [(int(round(item["center_x"])), int(round(item["center_y"]))) for item in history]
    if len(points) < 2:
        return
    for idx in range(1, len(points)):
        intensity = 80 + int(175 * idx / max(1, len(points) - 1))
        cv2.line(frame, points[idx - 1], points[idx], (0, intensity, 255), 2, cv2.LINE_AA)
    for point in points[-3:]:
        cv2.circle(frame, point, 3, (255, 255, 255), -1, cv2.LINE_AA)


def draw_overlay(frame, score: float, target_rect: dict, threshold: float, current_fps: float, best_mode: str, tracking_state: dict):
    output = frame.copy()
    hit = score >= threshold
    locked = tracking_state["locked"]
    bite_event = tracking_state["bite_event"]
    color = (0, 255, 255) if locked else ((0, 200, 0) if hit else (0, 0, 255))
    if bite_event:
        color = (0, 165, 255)

    top_left = (int(target_rect["left"]), int(target_rect["top"]))
    bottom_right = (int(target_rect["right"]), int(target_rect["bottom"]))
    cv2.rectangle(output, top_left, bottom_right, color, 2)

    search_region = tracking_state.get("active_search_region")
    if search_region is not None:
        search_top_left = (int(search_region["left"]), int(search_region["top"]))
        search_bottom_right = (
            int(search_region["left"] + search_region["width"]),
            int(search_region["top"] + search_region["height"]),
        )
        cv2.rectangle(output, search_top_left, search_bottom_right, (255, 255, 0), 1)

    for candidate in tracking_state.get("candidate_boxes", []):
        c_top_left = (int(candidate["left"]), int(candidate["top"]))
        c_bottom_right = (int(candidate["left"] + candidate["width"]), int(candidate["top"] + candidate["height"]))
        cv2.rectangle(output, c_top_left, c_bottom_right, (120, 255, 120), 1)

    draw_tracking_path(output, tracking_state["history"])

    bite_metrics = tracking_state["bite_metrics"]
    candidate_debug = tracking_state.get("candidate_debug", {})
    lines = [
        f"Score: {score:.4f} ({score * 100:.2f}%) | Template: {tracking_state['active_template_id'] or 'none'}",
        (
            f"Threshold: {threshold:.2f} | Track: {tracking_state['active_tracking_threshold']:.2f} | "
            f"Lock: {'ON' if locked else 'OFF'} | Mode: {best_mode}"
        ),
        (
            f"FPS: {current_fps:.2f} | Search: {tracking_state['search_source']} | "
            f"Valid: {'YES' if tracking_state['valid_detection'] else 'NO'} | Switches: {tracking_state['template_switches']}"
        ),
        (
            f"Mask: {tracking_state['candidate_mask_mode']} | HaloGate: {'ON' if tracking_state['halo_gate_enabled'] else 'OFF'} | "
            f"Candidates: {candidate_debug.get('gated_candidates', 0)}/{candidate_debug.get('raw_candidates', 0)}"
        ),
        (
            "DropWindow: "
            f"{bite_metrics['drop_pixels']:.1f}px | Speed: {bite_metrics['speed']:.2f}px/frame | "
            f"BITE: {'YES' if bite_event else 'NO'}"
        ),
        (
            f"Sound: {tracking_state['sound_level']:.1f}/{tracking_state['sound_threshold']:.1f} | "
            f"Monitor: {'ON' if tracking_state['sound_monitor_enabled'] else 'OFF'} | "
            f"State: {'TRIGGER' if tracking_state['sound_level'] > tracking_state['sound_threshold'] else 'IDLE'}"
        ),
        f"Fail: {tracking_state['failure_reason'] or 'none'} | MatchTemplate: {tracking_state['match_template_id'] or 'none'}",
    ]

    y = 25
    for line in lines:
        cv2.putText(output, line, (10, y), cv2.FONT_HERSHEY_SIMPLEX, 0.6, color if y < 60 else (255, 255, 255), 2, cv2.LINE_AA)
        y += 26

    if tracking_state.get("sound_notification_text") and tracking_state.get("sound_notification_until", 0.0) > time.monotonic():
        notice = tracking_state["sound_notification_text"]
        notice_top = max(10, output.shape[0] - 58)
        cv2.rectangle(output, (10, notice_top), (min(output.shape[1] - 10, 760), notice_top + 38), (0, 140, 255), -1)
        cv2.putText(output, notice, (18, notice_top + 26), cv2.FONT_HERSHEY_SIMPLEX, 0.7, (255, 255, 255), 2, cv2.LINE_AA)

    return output


def resize_to_height(image, target_height: int):
    if image.ndim == 2:
        image = cv2.cvtColor(image, cv2.COLOR_GRAY2BGR)
    if image.shape[0] == target_height:
        return image
    scale = target_height / image.shape[0]
    target_width = max(1, int(round(image.shape[1] * scale)))
    return cv2.resize(image, (target_width, target_height), interpolation=cv2.INTER_AREA)


def build_match_dashboard(frame, matched_overlay, result_map, match_info: dict, tracking_state: dict):
    panel_height = 220
    frame_panel = resize_to_height(frame, panel_height)
    template_panel = resize_to_height(match_info["template_image"], panel_height)
    overlay_panel = resize_to_height(matched_overlay, panel_height)
    normalized = cv2.normalize(result_map, None, 0, 255, cv2.NORM_MINMAX)
    heatmap = cv2.applyColorMap(normalized.astype(np.uint8), cv2.COLORMAP_JET)
    heatmap_panel = resize_to_height(heatmap, panel_height)
    processed_capture_panel = resize_to_height(prepare_variant(frame, match_info["best_mode"]), panel_height)
    processed_template_panel = resize_to_height(prepare_variant(match_info["template_image"], match_info["best_mode"]), panel_height)
    float_mask_panel = resize_to_height(match_info["float_mask"], panel_height)

    separator = np.full((panel_height, 12, 3), 30, dtype=np.uint8)
    dashboard = np.hstack(
        [
            frame_panel,
            separator,
            processed_capture_panel,
            separator,
            processed_template_panel,
            separator,
            float_mask_panel,
            separator,
            template_panel,
            separator,
            overlay_panel,
            separator,
            heatmap_panel,
        ]
    )

    panel_widths = [
        frame_panel.shape[1],
        processed_capture_panel.shape[1],
        processed_template_panel.shape[1],
        float_mask_panel.shape[1],
        template_panel.shape[1],
        overlay_panel.shape[1],
        heatmap_panel.shape[1],
    ]
    labels = [
        "Capture",
        "Processed Capture",
        "Processed Template",
        "Float Mask",
        f"Template: {match_info['template_id']}",
        "Tracked Overlay",
        "Heatmap",
    ]
    x = 10
    for width, label in zip(panel_widths, labels):
        cv2.putText(dashboard, label, (x, 25), cv2.FONT_HERSHEY_SIMPLEX, 0.75, (255, 255, 255), 2, cv2.LINE_AA)
        x += width + 12

    score_text = " | ".join(f"{name}={value:.4f}" for name, value in match_info["scores"].items())
    template_score_entries = match_info.get("template_scores", [])
    template_score_text = " | ".join(
        f"{entry['template_id']}={entry['score']:.4f}" for entry in template_score_entries[:6]
    )
    if len(template_score_entries) > 6:
        template_score_text += f" | +{len(template_score_entries) - 6} more"
    bite_metrics = tracking_state["bite_metrics"]
    candidate_debug = tracking_state.get("candidate_debug", {})
    footer_lines = [
        (
            f"Similarity: {match_info['score']:.4f} ({match_info['score'] * 100:.2f}%) | "
            f"Best: {match_info['best_mode']} | Template: {match_info['template_id']}"
        ),
        f"Scores: {score_text}",
        f"Template Scores: {template_score_text or 'n/a'}",
        (
            f"Lock={'ON' if tracking_state['locked'] else 'OFF'} | "
            f"LostFrames={tracking_state['lost_frames']} | TemplateMiss={tracking_state['template_miss_frames']} | "
            f"Switches={tracking_state['template_switches']} | Source={tracking_state['search_source']} | "
            f"Fail={tracking_state['failure_reason'] or 'none'}"
        ),
        (
            f"Mask={tracking_state['candidate_mask_mode']} | HaloGate={'ON' if tracking_state['halo_gate_enabled'] else 'OFF'} | "
            f"Candidates={candidate_debug.get('gated_candidates', 0)}/{candidate_debug.get('raw_candidates', 0)} | "
            f"RejectedHalo={candidate_debug.get('rejected_by_halo', 0)} | "
            f"Drop={bite_metrics['drop_pixels']:.1f}px | Speed={bite_metrics['speed']:.2f}px/frame | "
            f"Bite={'YES' if tracking_state['bite_event'] else 'NO'}"
        ),
        (
            f"Sound={tracking_state['sound_level']:.1f}/{tracking_state['sound_threshold']:.1f} | "
            f"Monitor={'ON' if tracking_state['sound_monitor_enabled'] else 'OFF'} | "
            f"State={'TRIGGER' if tracking_state['sound_level'] > tracking_state['sound_threshold'] else 'IDLE'} | "
            f"AudioError={tracking_state['sound_device_error'] or 'none'}"
        ),
    ]
    if tracking_state.get("sound_notification_text") and tracking_state.get("sound_notification_until", 0.0) > time.monotonic():
        footer_lines.append(f"Notification: {tracking_state['sound_notification_text']}")
    y = panel_height - 118
    for line in footer_lines:
        cv2.putText(dashboard, line, (10, y), cv2.FONT_HERSHEY_SIMPLEX, 0.55, (255, 255, 255), 2, cv2.LINE_AA)
        y += 24
    return dashboard


def should_exit_from_preview() -> bool:
    key = cv2.waitKey(1) & 0xFF
    return key in (27, ord("q"))


def announce_bite_event(match_info: dict, tracking_state: dict, frame_index: int, source_label: str) -> None:
    if not tracking_state.get("just_bite_event"):
        return

    bite_metrics = tracking_state["bite_metrics"]
    target_rect = match_info["target_rect"]
    timestamp = time.strftime("%H:%M:%S")
    print(
        f"[{timestamp}] BITE EVENT [{source_label}] frame={frame_index} "
        f"template={match_info['template_id']} score={match_info['score']:.4f} "
        f"drop={bite_metrics['drop_pixels']:.1f}px speed={bite_metrics['speed']:.2f}px/frame "
        f"center=({target_rect['center_x']:.1f},{target_rect['center_y']:.1f})",
        flush=True,
    )


